public class Gamer {

    int id;
    String FirstName;
    String LastName;
    int BirthYear;
    long IdentityNumber;

    public Gamer(int id, String firstName, String lastName, int birthYear, long identityNumber) {
        this.id = id;
        FirstName = firstName;
        LastName = lastName;
        BirthYear = birthYear;
        IdentityNumber = identityNumber;
    }
}
